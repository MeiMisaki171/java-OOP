package tinh_dong_goi;

public class Person {
	
	//instance variable
	private String cmnd;
	private String hoTen;
	
	public String getCmnd() {
		return cmnd;
	}
	
	//this tham chieu toi instance variable
	public void setCmnd(String cmnd) {
		this.cmnd = cmnd;
	}
	public String getHoTen() {
		return hoTen;
	}
	public void setHoTen(String hoTen) {
		this.hoTen = hoTen;
	}
}
